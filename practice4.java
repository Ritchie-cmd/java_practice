public class practice4 {
    public static void main(String[] args) {
        
        for(int g = 1; g <= 5; g++){
                for(int i = 1; i <= g; i++){
                    System.out.print(" * ");
                }
            System.out.println();
        }
        for(int s = 4; s >= 1; s--){
            for(int i = 1; i <= s; i++){
                System.out.print(" * ");
            }
            System.out.println();
        }
    }
}
